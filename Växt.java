package Inlämningsuppgift1;

public class Växt {

    private String name;
    private double length;



    public Växt(String name, double length) {
        this.name = name;
        this.length = length;

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

}

